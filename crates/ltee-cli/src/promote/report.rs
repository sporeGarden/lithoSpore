use pseudospore_core::DomainProfile;
use std::fmt::Write as _;
use std::fs;
use std::path::Path;

fn rmsd_limits(profile: Option<&DomainProfile>) -> (f64, f64) {
    profile.and_then(|p| p.tolerances.as_ref()).map_or(
        (
            litho_core::consts::DEFAULT_RMSD_KJ,
            litho_core::consts::DEFAULT_RMSD_KJ,
        ),
        |t| (t.tier1_rmsd_kj_max, t.tier2_rmsd_kj_max),
    )
}

pub(super) fn generate_expected_stub(module_name: &str, profile: Option<&DomainProfile>) -> String {
    let (tier1, tier2) = rmsd_limits(profile);
    format!(
        r#"{{
  "module": "{module_name}",
  "generated_from": "proof/outputs/{module_name}/",
  "acceptance_criteria": {{
    "tier1_rmsd_kj_max": {tier1},
    "tier2_rmsd_kj_max": {tier2},
    "basin_count_min": 1
  }},
  "notes": "Auto-generated by litho promote. Refine acceptance criteria manually."
}}
"#
    )
}

pub(super) fn generate_tolerances(scope: &toml::Table, profile: Option<&DomainProfile>) -> String {
    let (tier1, tier2) = rmsd_limits(profile);
    let mut output =
        String::from("# lithoSpore acceptance tolerances\n# Generated by `litho promote`\n\n");
    if profile.and_then(|p| p.tolerances.as_ref()).is_none() {
        output.push_str("# Generic RMSD limits — override via domain_profile.toml [tolerances]\n");
    }
    let _ = writeln!(
        output,
        "[global]\ntier1_rmsd_kj_max = {tier1}\ntier2_rmsd_kj_max = {tier2}\nchecksum_algorithm = \"BLAKE3\"\n"
    );

    // Derive module tolerances from scope.toml [[module]] entries
    if let Some(modules) = scope.get("module").and_then(|v| v.as_array()) {
        for module in modules {
            if let Some(name) = module.get("name").and_then(|v| v.as_str()) {
                let _ = writeln!(output, "[modules.\"{name}\"]");
                let _ = writeln!(output, "tier1_rmsd_kj_max = {tier1}");
                let _ = writeln!(output, "tier2_rmsd_kj_max = {tier2}\n");
            }
        }
    } else {
        output.push_str("# No modules found in scope.toml — add per-module tolerances manually\n");
    }

    output
}

